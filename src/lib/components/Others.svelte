<h2 id="ketentuan-berkas">Ketentuan Berkas Submission</h2>
<ul>
	<li>
		Berkas submission yang dikirim merupakan folder proyek dari MusicShelf API yang dikompres dalam
		bentuk <strong>ZIP</strong>.
	</li>
	<li>
		Pastikan di dalam folder proyek yang Anda kirim terdapat file seperti <code>package.json</code>
		dan <code>composer.json</code>.
	</li>
	<li>
		Pastikan Anda hapus dulu berkas <code>node_modules</code> dan <code>vendor</code> pada folder
		proyek sebelum mengkompresi dalam bentuk <strong>ZIP</strong>.
	</li>
</ul>

<h2>Submission Anda akan Ditolak bila</h2>
<ul>
	<li>Kriteria wajib MusicShelf API tidak terpenuhi.</li>
	<li>Ketentuan berkas submission tidak terpenuhi.</li>
	<li>Menggunakan database <strong>selain MySQL</strong>.</li>
	<li>Menggunakan Laravel <strong>versi dibawah 9</strong>.</li>
	<li>Menggunakan bahasa pemrograman dan teknologi lain, <strong>selain PHP</strong>.</li>
	<li>Menggunakan Framework PHP <strong>selain Laravel</strong>.</li>
	<li>Melakukan kecurangan seperti tindakan plagiasi.</li>
</ul>

